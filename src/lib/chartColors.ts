// 共用分類色（固定順序，不循環新增色相）。甜甜圈圖與長條圖需顯示同一組類別時，
// 依相同順序索引這個陣列，確保兩張圖對同一類別使用同一顏色。
// 前 5 色取自使用者指定的參考配色，經 dataviz 色票驗證工具做全部兩兩比對：
// 原本 6 色中的黃綠色和橙黃色對色盲使用者幾乎無法區分（ΔE 5.7），故拿掉黃綠色；
// 藍紫色和紫羅蘭色兩個紫色系太像，拿掉紫羅蘭色改用洋紅色；
// 橙紅色和洋紅色排相鄰時實際看起來仍太像（都偏暖紅色調），
// 改把橙黃色排在橙紅色旁邊（暖紅 vs 暖黃，辨識度更高），洋紅色移到最後一格；
// 洋紅色後來又改成淺藍色，跟藍紫色（第 1 色）算過色差 ΔE 21.2，足夠區分。
